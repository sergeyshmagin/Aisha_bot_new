            f"–î–æ—Å—Ç–∏–≥–Ω—É—Ç –º–∞–∫—Å–∏–º—É–º —Ñ–æ—Ç–æ. –ú–æ–∂–µ—Ç–µ —Ç–æ–ª—å–∫–æ –ø—Ä–æ–¥–æ–ª–∂–∏—Ç—å."
